/**
 * Writes Excel templates for pharmacy vs diagnostics partner uploads into public/templates/.
 * Run: npm run generate:partner-templates
 */
import { writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import XLSX from "xlsx";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..", "..");
const outDir = join(root, "public", "templates");

mkdirSync(outDir, { recursive: true });

const pharmacyHeaders = [
  "city",
  "state",
  "pharmacy_name",
  "chain",
  "address_line",
  "pincode",
  "lat",
  "lng",
  "drug_name",
  "generic_name",
  "strength",
  "form",
  "pack_size",
  "mrp_inr",
  "discount_pct",
  "price_inr",
  "price_type",
  "in_stock",
];

const pharmacyExample = [
  "Mumbai",
  "Maharashtra",
  "Apollo Pharmacy (Andheri)",
  "Apollo",
  "Near Metro",
  "400058",
  "19.114",
  "72.846",
  "Dolo 650 Tablet",
  "Paracetamol",
  "650 mg",
  "tablet",
  "15",
  "66.00",
  "10",
  "",
  "retail",
  "yes",
];

const pharmacySheet = XLSX.utils.aoa_to_sheet([pharmacyHeaders, pharmacyExample]);

const pharmacyBook = XLSX.utils.book_new();
XLSX.utils.book_append_sheet(pharmacyBook, pharmacySheet, "Prices");
const pharmacyPath = join(outDir, "paxmed-pharmacy-price-import-template.xlsx");
XLSX.writeFile(pharmacyBook, pharmacyPath);

const labHeaders = ["city", "state", "lab_name", "test_id", "mrp_inr", "discount_pct", "price_inr"];

const labExample = ["Mumbai", "Maharashtra", "Partner Lab Pvt Ltd", "1", "1200", "15", ""];

const labSheet = XLSX.utils.aoa_to_sheet([labHeaders, labExample]);
const labBook = XLSX.utils.book_new();
XLSX.utils.book_append_sheet(labBook, labSheet, "Lab prices");
const labPath = join(outDir, "paxmed-lab-price-import-template.xlsx");
XLSX.writeFile(labBook, labPath);

writeFileSync(
  join(outDir, "README.txt"),
  `PaxMed partner templates (generated by npm run generate:partner-templates)

paxmed-pharmacy-price-import-template.xlsx
  Upload: POST /api/import/prices/xlsx (multipart field: file)

paxmed-lab-price-import-template.xlsx
  Upload: POST /api/import/lab-prices/xlsx (multipart field: file)

discount_pct is percentage off MRP (0–100). Stored on pharmacy_prices.discount_pct / lab_test_prices.discount_pct.
`,
);

console.log("Wrote:", pharmacyPath);
console.log("Wrote:", labPath);
